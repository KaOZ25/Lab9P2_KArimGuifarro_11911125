    public void table() {
        conexion db = new conexion("./Usuarios.accdb");
        db.conectar();
        tabla.setModel(new javax.swing.table.DefaultTableModel(
                new Object[][]{},
                new String[]{
                    "ID", "Usuario", "Contrase√±a", "Nombre", "Edad", "Tipo"
                }
        ) {
            Class[] types = new Class[]{
                java.lang.Integer.class,
                java.lang.String.class,
                java.lang.String.class,
                java.lang.String.class,
                java.lang.Integer.class,
                java.lang.String.class
            };
            boolean[] canEdit = new boolean[]{
                false, false
            };

            @Override
            public Class getColumnClass(int columnIndex) {
                return types[columnIndex];
            }

            @Override
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit[columnIndex];
            }
        });
        try {
            db.query.execute("select * from Table1");
            ResultSet rs = db.query.getResultSet();
            while (rs.next()) {
                Object row[] = {rs.getInt(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getInt(5), rs.getString(6)};
                DefaultTableModel m
                        = (DefaultTableModel) tabla.getModel();
                m.addRow(row);
                tabla.setModel(m);
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        db.desconectar();
    }
    
